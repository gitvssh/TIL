package javareview2.util;

import java.util.*;

public class Test03_for {
	
	public static void main(String[] args){
		String arr1[]={"kim","lee","park"};
		
		for(String a:arr1){
			System.out.println(a);
		}
		System.out.println();
		
		int arr2[]={10,20,30,40,50};
		for(int b:arr2){
			System.out.println(b);
		}
	}
}
